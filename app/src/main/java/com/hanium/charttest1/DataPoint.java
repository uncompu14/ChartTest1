package com.hanium.charttest1;

public class DataPoint {
    float xValue, yValue;


    public DataPoint() {
    }

    public DataPoint(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public float getxValue() {
        return xValue;
    }

    public float getyValue() { return yValue; }

}

